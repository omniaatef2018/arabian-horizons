        <h2>Dubai City</h2>
        <div class="abt-dubai-text">
            <div>
                <p>Dubai is one of the seven Emirates that makeup the United Arab Emirates. It is the second largest
                    Emirate and has become world famous as a leading travel destination in the Middle East. Located in
                    the heart of the Arabian Desert, Dubai is a global metropolis that boasts of having the best of
                    everything. It is home to the world's most renowned sights that travelers from all around the world
                    come to see.</p>
                <p>You are spoilt for choices when it comes to the record breaking sights littered across the city such
                    as the tallest building in the world, the highest observation deck, the biggest aquarium, the
                    largest indoor mall, the biggest automated fountain, the largest indoor ski park, the tallest hotel
                    and so much more. </p>
                <p>While visiting Dubai, you will also get to enjoy world class entertainment destinations including the
                    biggest theme parks in the world, water parks and theatre shows that will add to your experience.
                    With so much in store, you are guaranteed a fulfilling and unforgettable vacation in Dubai.</p>
                <h3>Dubai Things to do</h3>
                <h4>Dubai Desert Safaris</h4>
                <p>Dubai is located within the Arabian Desert and as you can guess it's home to exceptional desert
                    safaris. You can choose to go on a morning desert safari to experience the desert sunrise, an
                    evening safari to see the golden sunset or even an overnight safari spent under the glittering stars
                    of the desert night sky. The tours includes fun activities such as dune bashing, sand boarding, quad
                    biking, buggy rides and camel rides. Not to mention, you also get to visit a Bedouin camp to learn
                    about the local Emirati traditions and enjoy the best of Arabian cuisine.</p>
                <h4>Dubai Theme Park Tours</h4>
                <p>Dubai has numerous theme parks that the entire family can visit for a day of fun and bonding. These
                    theme parks include IMG World of Adventures and Dubai Parks and Resorts; an integrated theme park
                    filled with numerous rides and rollercoasters for everyone. At Dubai Parks and Resorts you get to
                    meet your favorite hollywood heroes at Motiongate Dubai, experience the best of Indian cinema at
                    Bollywood Dubai or indulge your kids at the lego themed park at Legoland Dubai. You can also visit
                    Yas Island in Abu Dhabi, only a 2hr ride away from Dubai to experience the exciting thrills at
                    Ferrari World and Warner Bros Abu Dhabi.</p>
                <h4>Dubai WaterParks</h4>
                <p>To cool off from Dubai's desert heat, you can spend a day at one of Dubai's exceptional waterparks
                    made up of amazing rides and waterslides. You can take your pick to either visit Wild Wadi
                    waterpark, Legoland Waterpark, Aquaventure Waterpark or Yas Waterworld in Abu Dhabi.</p>
                <h4>Dubai Water Sports</h4>
                <p>Dubai offers exceptionally appealing water sports that make for an exciting and interactive
                    experience. You can cool off in the simmering desert heat by doing fun water sports on the waters of
                    the Persian Gulf. These include parasailing, jet skiing, flyboarding, banana boats, speedboats,
                    kayaking, kneeboarding, fly fist and more.</p>
                <h4>Dubai Sightseeing tours</h4>
                <p>Dubai is known for its groundbreaking achievements in the field of engineering. With the sightseeing
                    tours, you will get to see most of the city's renowned sights. These include Burj Khalifa, Burj Al
                    Arab, Palm Jumeirah, Dubai Frame and the World Islands. Also get to learn more about Emirati culture
                    with sightseeing tours to historical heritage sights and museums.</p>
                <h4>Dubai Dinner Cruises</h4>
                <p>A dinner cruise along the Dubai Creek or Dubai Marina is another thrilling way to explore the city.
                    It's an opportunity to ride an Arabian traditional dhow or luxury boat and sail across Dubai's water
                    to see some of the city's iconic sights. The cruise includes an exquisite dinner featuring authentic
                    Arabic cuisine and loads of entertainment.</p>
                <h4>Aerial Dubai City Tours</h4>
                <p>For a more enticing experience, go on a helicopter or seaplane tour and enjoy a bird's eye view of
                    the city. Plus you will get to see and marvel at Dubai's breathtaking sights including Palm
                    Jumeirah, the World Islands, Burj Khalifa, Burj Al Arab and more.</p>
